
/// Tween 的意思是在...之间，对动画中来说就是补间动画。给定一个起始值和结束值，
/// 之间的值根据进行的分率自动计算。比如起始值是20、结束值是80，
/// 那么当运动到 20% 时，对应的值就是：
/// 20 + (80 - 20)* 20% = 32
///
///




/// Color.lerp 可以根据计算出两个颜色对应分率处的颜色
